package com.howtoprogram.junit5.chainofresp;

public class ProductsMachine {

	private int gansitos;
	private int nitos;
	private int paletas;
	private double money;
	/**
	 * @return the gansitos
	 */
	public int getGansitos() {
		return gansitos;
	}
	/**
	 * @param gansitos the gansitos to set
	 */
	public void setGansitos(int gansitos) {
		this.gansitos = gansitos;
	}
	/**
	 * @return the nitos
	 */
	public int getNitos() {
		return nitos;
	}
	/**
	 * @param nitos the nitos to set
	 */
	public void setNitos(int nitos) {
		this.nitos = nitos;
	}
	/**
	 * @return the paletas
	 */
	public int getPaletas() {
		return paletas;
	}
	/**
	 * @param paletas the paletas to set
	 */
	public void setPaletas(int paletas) {
		this.paletas = paletas;
	}
	public double getMoney() {
		return money;
	}
	public void setMoney(double money) {
		this.money = money;
	}
	

}
